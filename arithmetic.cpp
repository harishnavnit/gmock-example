#include "arithmetic.h"

float Arithmetic::add(int x, int y)
{
    res = x + y;
    return res;
}

float Arithmetic::sub(int x, int y)
{
    res = x - y;
    return res;
}

float Arithmetic::mul(int x, int y)
{
    res = x * y;
    return res;
}

float Arithmetic::div(int x, int y)
{
    res = x / y;
    return res;
}
